const fromCurrency = document.getElementById('from-currency');
const toCurrency = document.getElementById('to-currency');
const fromFlag = document.getElementById('from-flag');
const toFlag = document.getElementById('to-flag');
const amountInput = document.getElementById('amount');
const msg = document.getElementById('msg');
const convertBtn = document.getElementById('convert-btn');

function populateCurrencies() {
  for (let currency in countryList) {
    let option1 = document.createElement('option');
    option1.value = currency;
    option1.textContent = currency;
    fromCurrency.appendChild(option1);

    let option2 = document.createElement('option');
    option2.value = currency;
    option2.textContent = currency;
    toCurrency.appendChild(option2);
  }
  fromCurrency.value = 'USD';
  toCurrency.value = 'INR';
  updateFlags();
}

function updateFlags() {
  const fromCode = countryList[fromCurrency.value];
  const toCode = countryList[toCurrency.value];
  fromFlag.src = `https://flagsapi.com/${fromCode}/flat/64.png`;
  toFlag.src = `https://flagsapi.com/${toCode}/flat/64.png`;
}

async function getExchangeRate() {
  const amount = parseFloat(amountInput.value);
  const from = fromCurrency.value;
  const to = toCurrency.value;

  if (isNaN(amount) || amount <= 0) {
    msg.textContent = 'Please enter a valid amount';
    return;
  }

  try {
    const response = await fetch(`https://api.exchangerate-api.com/v4/latest/${from}`);
    const data = await response.json();
    const rate = data.rates[to];
    const convertedAmount = (amount * rate).toFixed(2);
    msg.textContent = `${amount} ${from} = ${convertedAmount} ${to}`;
  } catch (error) {
    msg.textContent = 'Error fetching exchange rate';
    console.error(error);
  }
}

fromCurrency.addEventListener('change', updateFlags);
toCurrency.addEventListener('change', updateFlags);
convertBtn.addEventListener('click', (e) => {
  e.preventDefault();
  getExchangeRate();
});

populateCurrencies();
